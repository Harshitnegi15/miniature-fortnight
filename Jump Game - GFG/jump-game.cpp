class Solution {
  public:
    int canReach(int A[], int N) {
   
        int jump=A[0];
        for(int i=0;i<N;i++){
            if(A[i]==0 && jump<=i)
            return 0;
            if(jump<A[i]+i)
                jump=A[i]+i;
            
            if(jump>=N-1)
            return 1;
        }
        return 0;
    }
};

